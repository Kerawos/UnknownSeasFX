package pl.mareksowa.models;

public class Price {

    private int food;
    private int wheat;
    private int clothes;
    private int wine;
    private int mahogany;
    private int decorations;
    private int silk;
    private int spices;

    public int getFood() {
        return food;
    }

    public void setFood(int food) {
        this.food = food;
    }

    public int getWheat() {
        return wheat;
    }

    public void setWheat(int wheat) {
        this.wheat = wheat;
    }

    public int getClothes() {
        return clothes;
    }

    public void setClothes(int clothes) {
        this.clothes = clothes;
    }

    public int getWine() {
        return wine;
    }

    public void setWine(int wine) {
        this.wine = wine;
    }

    public int getMahogany() {
        return mahogany;
    }

    public void setMahogany(int mahogany) {
        this.mahogany = mahogany;
    }

    public int getDecorations() {
        return decorations;
    }

    public void setDecorations(int decorations) {
        this.decorations = decorations;
    }

    public int getSilk() {
        return silk;
    }

    public void setSilk(int silk) {
        this.silk = silk;
    }

    public int getSpices() {
        return spices;
    }

    public void setSpices(int spices) {
        this.spices = spices;
    }
}
